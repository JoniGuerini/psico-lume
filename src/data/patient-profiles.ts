export function buildDefaultSchedule(patient: {
  status: Patient["status"]
  sessionDay: string
  sessionTime: string
  sessionDuration: number
  modality: Patient["modality"]
}): PatientSchedule[] | undefined {
  if (patient.status !== "ativo" || !patient.sessionTime || !patient.sessionDay) {
    return undefined
  }

  const weekday = WEEKDAY_MAP[patient.sessionDay] ?? patient.sessionDay
  const modality =
    patient.modality === "hibrido" ? "presencial" : patient.modality

  return [
    {
      weekday,
      time: patient.sessionTime,
      duration: String(patient.sessionDuration),
      modality,
    },
  ]
}

const WEEKDAY_MAP: Record<string, string> = {
  Seg: "Segunda",
  Ter: "Terça",
  Qua: "Quarta",
  Qui: "Quinta",
  Sex: "Sexta",
  Sáb: "Sábado",
  Dom: "Domingo",
}
